// Há eleições em seu país! Em uma pequena cidade, existem apenas 50 eleitores.
// Use um loop for para simular a votação de 50 pessoas, registrando uma string como esta para
// o console (para os números 1 a 50): 'O eleitor número 1 está votando no momento'

for (let i = 1; i <= 50; i += 1) {
  console.log(`O eleitor número ${i} está votando no momento`);
}
